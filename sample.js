
/**
* @description This function takes two arguments `x` and `y`, and returns their sum.
* 
* @param { object } x - In the given function `add`, the `x` input parameter is
* passed through to the function body as a argument and is used along with the `y`
* input parameter to calculate the sum of both parameters and return the result.
* 
* @param {  } y - In the given function `add = (x', y) => x' + y`, the `y` input
* parameter is used as a second argument to be added to the first argument `x'`.
*/
const add = (x, y) => x + y;

/**
* @description This function takes two arguments `x` and `y`, and returns the
* difference between `x` and `y`.
* 
* @param { number } x - In the given function `sub = (x`, `y) => x - y`, the `x`
* input parameter is the first operand of the subtraction operation.
* 
* @param { number } y - In the function `sub = (x , y) => x - y`, the `y` input
* parameter is used as a second operand to subtract from the `x` operand.
*/
const sub = (x, y) => x - y;

/**
* @description The function `mul` takes two arguments `x` and `y` and returns their
* product (the result of multiplying `x` by `y`).
* 
* @param { number } x - In the function `mul=(x.y)=> x*y`, the input parameter `x`
* multiplies with the input parameter `y` to calculate the product `x*y`.
* 
* @param { number } y - The `y` input parameter multiplies with the `x` input parameter
* inside the function.
*/
const mul = (x, y) => x * y;

/**
* @description This function divides the first argument `x` by the second argument
* `y`, but returns 0 if `y` is equal to 0.
* 
* @param { number } x - The `x` input parameter divides the result by `y`, when `y`
* is not equal to zero.
* 
* @param { number } y - The `y` input parameter is used to determine whether the
* function should return `0` or the result of dividing `x` by `y`.
*/
const div = (x, y) => y === 0 ? 0 : x / y;






