/**
* @description The function `mul` takes two arguments `x` and `y`, and returns their
* product (i.e., `x * y`).
* 
* @param {  } x - In the function `mul = (x², y) => x × y`, the `x` input parameter
* multiplies with the `y` input parameter to produce the output of the function.
* 
* @param { number } y - In the function `mul = (x，y) => x * y`, the `y` input parameter
* is multiplied by `x`.
*/
const mul = (x, y) => x * y;

