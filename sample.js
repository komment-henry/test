
/**
* @description The function `add` takes two arguments `x` and `y`, and returns their
* sum.
* 
* @param {  } x - In the function `add=(x.,y)=>x+y`, the `x` input parameter is the
* first number that is being added to the second number `y`.
* 
* @param { number } y - In the given function `add=(x,...,y)`, the input parameter
* `y` adds its value to `x`.
*/
const add = (x, y) => x + y;

/**
* @description The given function `sub` takes two arguments `x` and `y` and returns
* the difference between `x` and `y`.
* 
* @param { number } x - In the given function `sub`, the `x` input parameter is
* passed to the arrow function's `x - y` expression and serves as the first operand
* for the subtraction operation.
* 
* @param {  } y - The `y` input parameter subtracts its value from the `x` parameter
* within the function.
*/
const sub = (x, y) => x - y;

/**
* @description The function `mul` takes two arguments `x` and `y`, and returns their
* product (`x * y`).
* 
* @param {  } x - The `x` input parameter is multiplied by the `y` input parameter
* and combined to produce the output of the function.
* 
* @param { number } y - The `y` input parameter is the multiplier.
*/
const mul = (x, y) => x * y;

/**
* @description The given function `div` takes two arguments `x` and `y`, and returns
* the result of dividing `x` by `y`, but if `y` is equal to 0 then it returns 0
* instead of throwing an error or undefined value.
* 
* @param { number } x - The `x` input parameter is not used at all within the
* function's body because the condition `y === 0` always returns `true`, resulting
* only for a value of `div(x ,y)` equals 0 if `y==0`.
* 
* @param { number } y - In the given function `div`, the `y` input parameter is used
* as a denominator for the fraction `x / y`.
*/
const div = (x, y) => y === 0 ? 0 : x / y;








